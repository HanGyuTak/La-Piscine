void	ft_advanced_sort_string_tab(char **tab, int (*cmp)(char *, char *))
{
	int		i;
	int		j;
	char	*tmp;
	int		cnt;

	cnt = 0;
	while (tab[cnt])
		cnt++;
	i = 0;
	while (i < cnt - 1)
	{
		j = 0;
		while (j < cnt - 1)
		{
			if (cmp(tab[j], tab[j + 1]) > 0)
			{
				tmp = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = tmp;
			}
			j++;
		}
		i++;
	}
}

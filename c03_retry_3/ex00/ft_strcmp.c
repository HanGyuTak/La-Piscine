#include<unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] == s2[i])
			i++;
		else
			return (s1[i] - s2[i]);
	}
	if (s1[i] != '\0' && s2[i] == '\0')
		return (s1[i]);
	else if (s1[i] == '\0' && s2[i] != '\0')
		return (-1 * s2[i]);
	return (0);
}
